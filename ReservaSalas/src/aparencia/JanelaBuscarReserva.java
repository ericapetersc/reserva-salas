    public JanelaBuscarReserva() {
        initComponents();
        Reserva[] reservas = new Reserva[negocio.NegocioFacade.regatarReservas().length];
        reservas = negocio.NegocioFacade.regatarReservas();
        for (int i = reservas.length - 1; i >= 0; i--) {
            combo.addItem(reservas[i].getCodigo() + " - " + reservas[i].getUsuario().getNome());
        }
        if (reservas.length==0){
            JOptionPane.showMessageDialog(null, "Não há nenhuma reserva cadastrada!");
        }
        AutoCompleteDecorator.decorate(combo);
    }

