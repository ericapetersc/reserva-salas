    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        //Remove o tipo da sala do valor selecionado do comboBox
        String valorCombo = combo.getSelectedItem().toString();
        int pos = valorCombo.indexOf(" - ");
        String substring = valorCombo.substring(0, pos);
        
        Reserva r = negocio.NegocioFacade.consultarReserva(substring);
        
        cod_reserva.setText(r.getCodigo());
        responsavel.setText(r.getUsuario().getNome() + " " + r.getUsuario().getSobrenome());
        bloco.setText(r.getBloco().getLetra());
        sala.setText(String.valueOf(r.getSala().getNumero()));
        tipo.setText(r.getSala().getTipo().toString());
        periodo.setText(r.getPeriodo());
        data.setText(r.getData());
    }//GEN-LAST:event_BuscarActionPerformed

