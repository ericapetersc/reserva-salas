    public void carregarSalas(){
        Bloco[] blocos = negocio.NegocioFacade.resgatarBlocos();
        
        TableModel tb = tabelaSalas.getModel();
        if( !(tb instanceof DefaultTableModel) )
            return;
        
        DefaultTableModel dtb = (DefaultTableModel) tb;
        dtb.removeRow(0);
        Object linha[] = new Object[3];
        
        Sala[] salass = negocio.NegocioFacade.resgatarSalas(blocos[0]);
        
        linha[0] = blocos[0].getLetra();
        for( Bloco b : blocos){
            Sala[] salas = negocio.NegocioFacade.resgatarSalas(b);
            linha[0] = b.getLetra();
            for( Sala s : salas){
                linha[0] = b.getLetra();
                linha[1] = s.getNumero();
                linha[2] = s.getTipo().toString();
                dtb.addRow( linha );
            }
        }
    }

