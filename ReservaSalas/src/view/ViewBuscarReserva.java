    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        //Remove o codigo do nome no combo
        String valorCombo = combo.getSelectedItem().toString();
        valorCombo.replace( "-", "");
        valorCombo.replace( ".", "");
        int pos = valorCombo.indexOf(" - ");
        this.codigo = valorCombo.substring(0, pos);
        int idSala = controller.consultaReserva();
        controller.consultarSala(idSala);
    }//GEN-LAST:event_BuscarActionPerformed

