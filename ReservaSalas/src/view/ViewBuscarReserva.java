    public void popularComboBox(){
        combo.removeAllItems();
        List<Reserva> reservas = controller.listaReservas();
        if (reservas.size()==0){
            JOptionPane.showMessageDialog(null, "Não há nenhuma reserva cadastrada!");
        }
        else{
            while (!reservas.isEmpty()){
                Reserva r = reservas.get(0);
                combo.addItem(r.getCodigo() + " - " + format("###-###-###.##", r.getCPF()));
                reservas.remove(0);
            }
            AutoCompleteDecorator.decorate(combo);
        }
    }
    
     public void LimparTela(){
        cod_reserva.setText("");
        responsavel.setText("");
        bloco.setText("");
        sala.setText("");
        tipo.setText("");
        periodo.setText("");
        data.setText("");
    }
    
    public void setCampos(Reserva r){
        cod_reserva.setText(Integer.toString(r.getCodigo()));
        responsavel.setText(format("###-###-###.##", r.getCPF()));
        periodo.setText(r.getPeriodo());
        //adicionando mascara
        data.setText(format("##/##/####", r.getData()));
        combo.setSelectedItem(r.getCodigo() + " - " + r.getCPF());
    }
    
     public void setCamposSala(Sala s){
        if (s.getTipo()==0){
            tipo.setText("Laboratório");
        }else {
            tipo.setText("Normal");
        }
        sala.setText(Integer.toString((s.getNumero())));
        bloco.setText(s.getBloco());
    }
    
    private static String format(String pattern, Object value) { 
        MaskFormatter mask; 
        try { 
            mask = new MaskFormatter(pattern); 
            mask.setValueContainsLiteralCharacters(false); 
            return mask.valueToString(value); 
        } catch (ParseException e) { 
            throw new RuntimeException(e); 
        } 
    } 
    
    public void avisoSetCampos(){
        JOptionPane.showMessageDialog(null, "Houve um erro. Não foi possível buscar os dados desse cpf.");
    }
    
